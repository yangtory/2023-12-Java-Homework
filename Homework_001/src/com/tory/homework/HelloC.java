/*
 * println 으로 정수 30, 40의 4칙연산 출력하기 
 */
public class HelloC {
	
	public static void main(String[] args) {
		int num1 = 30;
		int num2 = 40;
		System.out.println(num1 + "+" + num2 + "=" + (num1+num2));
		System.out.println(num1 + "-" + num2 + "=" + (num1-num2));
		System.out.println(num1 + "x" + num2 + "=" + (num1*num2));
		System.out.println(num1 + "÷" + num2 + "=" + (num1/num2));
	}

}
